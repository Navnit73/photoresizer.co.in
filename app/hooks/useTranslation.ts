export function useTranslation() {
  const pathname = usePathname() || '';
  const lang = pathname.startsWith('/de') ? 'de' : 
               pathname.startsWith('/fr') ? 'fr' : 
               pathname.startsWith('/es') ? 'es' : 'en';
  
  return {
    t: translations[lang as keyof typeof translations] || translations.en,
    lang
  };
}
